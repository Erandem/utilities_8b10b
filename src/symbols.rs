/// All possible control codes
#[repr(u8)]
#[derive(Debug, Copy, Clone, PartialEq, Eq)]
#[cfg_attr(feature = "ufmt", derive(ufmt::derive::uDebug))]
pub enum ControlChars {
    K23_7 = 0xF7,
    K27_7 = 0xFB,

    K28_0 = 0x1C,
    K28_1 = 0x3C,
    K28_2 = 0x5C,
    K28_3 = 0x7C,
    K28_4 = 0x9C,
    K28_5 = 0xBC,
    K28_6 = 0xDC,
    K28_7 = 0xFC,

    K29_7 = 0xFD,
    K30_7 = 0xFE,
}

impl TryFrom<u8> for ControlChars {
    type Error = u8;

    fn try_from(value: u8) -> Result<Self, Self::Error> {
        use ControlChars::*;

        match value {
            0xF7 => Ok(K23_7),
            0xFB => Ok(K27_7),

            0x1C => Ok(K28_0),
            0x3C => Ok(K28_1),
            0x5C => Ok(K28_2),
            0x7C => Ok(K28_3),
            0x9C => Ok(K28_4),
            0xBC => Ok(K28_5),
            0xDC => Ok(K28_6),
            0xFC => Ok(K28_7),

            0xFD => Ok(K29_7),
            0xFE => Ok(K30_7),

            other => Err(other),
        }
    }
}

